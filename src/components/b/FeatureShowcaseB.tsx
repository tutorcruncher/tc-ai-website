function FeatureCard({ feature, index }: { feature: typeof FEATURES[0]; index: number }) {
  const ref = useRef<HTMLDivElement>(null);
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
          observer.disconnect();
        }
      },
      { threshold: 0.2 }
    );

    if (ref.current) {
      observer.observe(ref.current);
    }

    return () => observer.disconnect();
  }, []);

  return (
    <motion.div
      ref={ref}
      initial={{ opacity: 0, y: 30 }}
      animate={isVisible ? { opacity: 1, y: 0 } : { opacity: 0, y: 30 }}
      transition={{ duration: 0.5, delay: index * 0.1, ease: "easeOut" }}
      className="bg-white rounded-2xl border border-default p-6 hover:shadow-lg transition-shadow"
    >
      <div className="w-12 h-12 bg-page rounded-xl flex items-center justify-center text-link mb-4">
        {feature.icon}
      </div>
      <h3 className="font-heading text-xl font-semibold text-primary mb-2">
        {feature.title}
      </h3>
      <p className="text-muted-dark text-sm leading-relaxed">
        {feature.description}
      </p>
    </motion.div>
  );
}

